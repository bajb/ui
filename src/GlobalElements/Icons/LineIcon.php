  protected $_icon;

  public static function create($icon)
  {
    if(!in_array($icon, static::$_icons))
    {
      throw new \Exception(
        'Icon: "' . $icon . '" is not supported by LineIcon'
      );
    }

    $icn = new static;
    $icn->_icon = $icon;
    return $icn;
  }

  protected function _processIconIncludes(AssetManager $assetManager)
  {
    $assetManager->requireCss('assets/css/GlobalElements/LineIcons');
  }

  /**
   * @return HtmlTag
   */
  protected function _produceHtml()
  {
    $icon = parent::_produceHtml();
    $icon->addClass('lineIcon', 'lineIcon-' . $this->_icon);
    return $icon;
  }

  public static function getIcons()
  {
    $icons = array_combine(
      self::$_icons,
      self::getIconsParsed()
    );
    natcasesort($icons);
    return $icons;
  }

  protected static function getIconsParsed()
  {
    $icons = [];
    foreach(self::$_icons as $icon)
    {
      $icon = str_replace('-', ' ', $icon);
      $icons[] = ucwords($icon);
    }
    return $icons;
  }
