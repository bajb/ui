  protected $_icon;

  public static function create($icon)
  {
    if(!isset(static::$_icons[$icon]))
    {
      throw new \Exception(
        'Icon: "' . $icon . '" is not supported by LineIcon'
      );
    }

    $icn = new static;
    $icn->_icon = $icon;
    return $icn;
  }

  protected function _processIconIncludes(ResourceManager $resourceManager)
  {
    $resourceManager->requireCss('assets/css/GlobalElements/LineIcons.css');
  }

  /**
   * @return HtmlTag
   */
  protected function _produceHtml()
  {
    $icon = parent::_produceHtml();
    $icon->addClass('lineIcon', 'lineIcon-' . $this->_icon);
    return $icon;
  }

  public static function getIcons()
  {
    return self::$_icons;
  }
